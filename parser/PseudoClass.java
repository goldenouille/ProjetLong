package parser;

public class PseudoClass {
        private String name;
        private int id;
        
        public void setName(String s) {
                this.name = s;
        }
        
        public string getName() {
                return this.name;
        }
        
        public void setId(int n) {
                this.id = n;
        }
        
        public int getId() {
                return this.id;
        }
}
